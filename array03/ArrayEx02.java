package array03;


/*
 * for문의 시작.
 * 1. 변수 초기화(i=value)
 * 2. 조건 확인
 * 3. 실행문 실행
 * 4. stack 끝날 때 증감문 실행
 * 5. 증감문이 적용된 변수의 값 조건 재확인
 */

public class ArrayEx02 {

	public static void main(String[] args) {
		
		for (int i = 5; i < 10; i++) {
			System.out.println(i);
			
		} //증감은 stack가 끝날 때

	}

}
